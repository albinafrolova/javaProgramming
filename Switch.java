public class Switch {

    public static void main(String[] args) {
       char m='a';
       switch(m){
           case 'a': System.out.print("1");
           case 'b': System.out.print("2");
           case 'c': System.out.print("3");           
    }
       System.out.println(" Done");
    }
    
}
